/*	Functie: VGA_placeBMP
 *  Parameters:
 *  - coördinaat x;
 *  - coördinaat y;
 *  - filenaam van bmp
 *
 *  Return value: -
 *
 *  Auteur: Koen Gort
 *  Externe bron: http://www.digole.com/tools/PicturetoC_Hex_converter.php
 */
void vga_place_bmp(uint16_t xp, uint16_t yp, struct bmp_file *bmp)
{
	const uint32_t start_address = 0x080E0000; // moet 32 bits zijn!
											   // 080E0000 = sector 11 (128 kbyte)
											   // 080E0000 = 135 135 232
	// coordinaten:
	if( (xp < 0) || (yp < 0) )
	{
		// UART_puts();
		//return ERROR_OUT_OF_BOUNDARIES;
	}
	else
	{
		uint8_t flash_data[SIZE_OF_BUFFER] = {0}; // sizeof(hele array) met buffer van max. hele scherm

		FLASH_Unlock();
		FLASH_ClearFlag( FLASH_FLAG_EOP    |
						 FLASH_FLAG_WRPERR |
						 FLASH_FLAG_PGAERR |
						 FLASH_FLAG_PGPERR |
						 FLASH_FLAG_PGSERR
				    	);

		FLASH_EraseSector(FLASH_Sector_11, VoltageRange_1); // VoltageRange_1 voor bytes

		uint16_t i,j; // tellers

		// write data to flash
		for(i=0; i<(*(bmp->hoogte) * *(bmp->breedte)); i++)   // moet worden: sizeof(hele array)
			FLASH_ProgramByte(start_address + (i*sizeof(*(bmp->bmp_data))), *(bmp->bmp_data+i)); // sizeof(1 element van array)

		for(i=0; i<(*(bmp->hoogte) * *(bmp->breedte)); i++) // read data
			flash_data[i] = *(uint8_t *)(start_address + ( i*sizeof(*(bmp->bmp_data)) )); //

		FLASH_Lock();

		// waarde schrijven naar VGA:
		for(i=0; i<(*bmp->breedte); i++)
			for(j=0; j<(*bmp->hoogte); j++)
				UB_VGA_SetPixel(xp+i,yp+j, flash_data[j*(*bmp->breedte)+i]);

	} // einde if/else
} // einde functie
